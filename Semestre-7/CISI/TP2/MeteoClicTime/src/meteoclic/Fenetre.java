    private void activation() {
        switch (sf) {
            case E1:
                bStart.setEnabled(true);
                bStop.setEnabled(false);
                timer.stop();
                break;
            case E2:
                bStart.setEnabled(false);
                bStop.setEnabled(true);
                timer.restart();
                break;
        }
    }

